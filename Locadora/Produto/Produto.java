
class Produto {
	private String nome; 
	private int numfaixas;
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public String getNome() {
		return nome;
	}
	
	public void setNumFaixas(int numfaixas) {
		this.numfaixas = numfaixas;
	}
	
	public int getNumFaixas() {
		return numfaixas;
	}	
}
